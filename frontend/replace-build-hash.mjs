import { execSync } from 'node:child_process'
import { mkdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const frontendDir = dirname(fileURLToPath(import.meta.url))
const generatedDir = resolve(frontendDir, 'src/generated')
const generatedFile = resolve(generatedDir, 'buildHash.ts')

function getBuildHash() {
  const fromEnv = process.env.SUB2API_BUILD_HASH?.trim()
  if (fromEnv) {
    return fromEnv
  }

  try {
    return (
      execSync('git rev-parse --short HEAD', {
        cwd: frontendDir,
        encoding: 'utf8',
        stdio: ['ignore', 'pipe', 'ignore']
      }).trim() || 'unknown'
    )
  } catch {
    return 'unknown'
  }
}

function getBuildTime() {
  return process.env.SUB2API_BUILD_TIME?.trim() || ''
}

const buildHash = getBuildHash()
const buildTime = getBuildTime()
const content = `// Generated by frontend/replace-build-hash.mjs. Do not edit.
export const BUILD_HASH = ${JSON.stringify(buildHash)}
export const BUILD_TIME = ${JSON.stringify(buildTime)}
`

mkdirSync(generatedDir, { recursive: true })
writeFileSync(generatedFile, content)
console.log(`[build-hash] generated ${generatedFile} with ${buildHash}${buildTime ? `, built at ${buildTime}` : ''}`)
